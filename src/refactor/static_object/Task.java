package refactor.static_object;

public class Task {

    private Long id;
    private String body;

    public Long getId() {
        return id;
    }


    public void setId(Long id) {
        this.id = id;
    }


    public void setBody(String body) {
        this.body = body;
    }


    public String getBody() {
        return body;
    }
}
